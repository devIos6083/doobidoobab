/// 단어 데이터 모델
///
/// 퀴즈에 사용될 단어 정보를 담는 클래스입니다.
class Word {
  final String? id; // New field for Firebase document ID
  final String word;
  final String meaning;
  final String category;
  final String difficulty;
  final String? hint;
  final String? imageUrl;

  Word({
    this.id, // Optional ID parameter
    required this.word,
    required this.meaning,
    required this.category,
    required this.difficulty,
    this.hint,
    this.imageUrl,
  });

  // Updated fromMap to include id
  factory Word.fromMap(Map<String, dynamic> map) {
    return Word(
      id: map['id'],
      word: map['word'],
      meaning: map['meaning'],
      hint: map['hint'],
      category: map['category'],
      difficulty: map['difficulty'],
      imageUrl: map['imageUrl'],
    );
  }

  // Updated toMap to include id
  Map<String, dynamic> toMap() {
    return {
      'id': id,
      'word': word,
      'meaning': meaning,
      'hint': hint,
      'category': category,
      'difficulty': difficulty,
      'imageUrl': imageUrl,
    };
  }

  // Updated fromFirestore to also capture document ID
  factory Word.fromFirestore(Map<String, dynamic> data, String documentId) {
    return Word(
      id: documentId,
      word: data['word'] ?? '',
      meaning: data['meaning'] ?? '',
      category: data['category'] ?? '',
      difficulty: data['difficulty'] ?? '보통',
      hint: data['hint'],
      imageUrl: data['imageUrl'],
    );
  }

  // Exclude ID from toFirestore as Firebase handles document IDs separately
  Map<String, dynamic> toFirestore() {
    return {
      'word': word,
      'meaning': meaning,
      'category': category,
      'difficulty': difficulty,
      'hint': hint,
      'imageUrl': imageUrl,
    };
  }
}


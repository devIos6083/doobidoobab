/// 난이도에 따라 단어 필터링하는 함수
List<Word> filterWordsByDifficulty(String difficulty) {
  if (difficulty == '쉬움') {
    return allWords.where((word) => word.difficulty == '쉬움').toList();
  } else if (difficulty == '보통') {
    return allWords
        .where((word) => word.difficulty == '쉬움' || word.difficulty == '보통')
        .toList();
  } else {
    // 어려움 난이도는 모든 단어 포함
    return List.from(allWords);
  }
}
